CREATE OR REPLACE FUNCTION public.orderfeedback_createfromqrtoken(
    p_qrtoken text,
    p_orderid uuid,
    p_orderfeedbackid uuid,
    p_rating integer,
    p_comment text
)
RETURNS TABLE(
    "OrderFeedbackId" uuid,
    "TenantId" uuid,
    "BranchId" uuid,
    "OrderId" uuid,
    "CustomerId" uuid,
    "Rating" integer,
    "Comment" varchar,
    "CreatedAtUtc" timestamptz
)
LANGUAGE plpgsql AS $$
DECLARE
    order_context record;
BEGIN
    SELECT
        o."TenantId",
        o."BranchId",
        o."OrderId",
        o."CustomerId",
        o."CustomerName",
        o."CustomerWhatsApp"
    INTO order_context
    FROM "Orders" o
    JOIN "BranchTables" bt ON bt."TableId" = o."TableId"
    WHERE o."OrderId" = p_orderid
      AND bt."QrToken" = p_qrtoken;

    IF NOT FOUND THEN
        PERFORM public.raise_app_error(51709);
    END IF;

    RETURN QUERY
    WITH saved AS (
        INSERT INTO "OrderFeedback" (
            "OrderFeedbackId",
            "TenantId",
            "BranchId",
            "OrderId",
            "Rating",
            "Comment",
            "CustomerName",
            "CustomerWhatsApp"
        )
        VALUES (
            p_orderfeedbackid,
            order_context."TenantId",
            order_context."BranchId",
            p_orderid,
            p_rating,
            p_comment,
            order_context."CustomerName",
            order_context."CustomerWhatsApp"
        )
        ON CONFLICT ("OrderId") DO UPDATE SET
            "Rating" = EXCLUDED."Rating",
            "Comment" = EXCLUDED."Comment",
            "CustomerName" = EXCLUDED."CustomerName",
            "CustomerWhatsApp" = EXCLUDED."CustomerWhatsApp"
        RETURNING *
    )
    SELECT
        saved."OrderFeedbackId",
        saved."TenantId",
        saved."BranchId",
        saved."OrderId",
        order_context."CustomerId",
        saved."Rating",
        saved."Comment",
        saved."CreatedAtUtc"
    FROM saved;
END;
$$;

