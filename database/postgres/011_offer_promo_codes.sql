-- Adds promo-code offers, usage limits, and offer redemption tracking.

ALTER TABLE "BranchOffers"
ADD COLUMN IF NOT EXISTS "PromoCode" varchar(40) NULL,
ADD COLUMN IF NOT EXISTS "RequiresPromoCode" boolean NOT NULL DEFAULT false,
ADD COLUMN IF NOT EXISTS "MaxTotalRedemptions" integer NULL,
ADD COLUMN IF NOT EXISTS "MaxRedemptionsPerCustomer" integer NULL,
ADD COLUMN IF NOT EXISTS "MaxRedemptionsPerDay" integer NULL;

CREATE UNIQUE INDEX IF NOT EXISTS "UX_BranchOffers_BranchId_PromoCode"
ON "BranchOffers" ("BranchId", lower("PromoCode"))
WHERE "PromoCode" IS NOT NULL;

CREATE TABLE IF NOT EXISTS "BranchOfferRedemptions" (
    "BranchOfferRedemptionId" uuid PRIMARY KEY,
    "RowId" bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    "TenantId" uuid NOT NULL REFERENCES "Tenants" ("TenantId") ON DELETE CASCADE,
    "BranchId" uuid NOT NULL REFERENCES "Branches" ("BranchId") ON DELETE CASCADE,
    "BranchOfferId" uuid NOT NULL REFERENCES "BranchOffers" ("BranchOfferId"),
    "OrderId" uuid NOT NULL REFERENCES "Orders" ("OrderId") ON DELETE CASCADE,
    "CustomerId" uuid NULL REFERENCES "Customers" ("CustomerId") ON DELETE SET NULL,
    "CustomerWhatsApp" varchar(32) NULL,
    "PromoCode" varchar(40) NULL,
    "RedemptionTypeCode" varchar(32) NOT NULL,
    "SubtotalAmount" numeric(10,2) NOT NULL DEFAULT 0,
    "DiscountAmount" numeric(10,2) NOT NULL DEFAULT 0,
    "FinalTotalAmount" numeric(10,2) NOT NULL DEFAULT 0,
    "RedeemedAtUtc" timestamptz NOT NULL DEFAULT public.app_now(),
    CONSTRAINT "UQ_BranchOfferRedemptions_OrderId" UNIQUE ("OrderId"),
    CONSTRAINT "CK_BranchOfferRedemptions_RedemptionTypeCode" CHECK ("RedemptionTypeCode" IN ('AutoApplied','PromoCode'))
);

CREATE INDEX IF NOT EXISTS "IX_BranchOfferRedemptions_Offer_RedeemedAtUtc"
ON "BranchOfferRedemptions" ("BranchOfferId", "RedeemedAtUtc" DESC);

CREATE INDEX IF NOT EXISTS "IX_BranchOfferRedemptions_Branch_RedeemedAtUtc"
ON "BranchOfferRedemptions" ("TenantId", "BranchId", "RedeemedAtUtc" DESC);

CREATE OR REPLACE FUNCTION public.branchoffer_create(
    p_tenantid uuid,
    p_branchid uuid,
    p_branchofferid uuid,
    p_title text,
    p_subtitle text,
    p_discounttext text,
    p_imageurl text,
    p_imagealttext text,
    p_displayorder integer,
    p_startsatutc timestamptz,
    p_endsatutc timestamptz,
    p_discounttypecode text,
    p_discountvalue numeric,
    p_minimumorderamount numeric,
    p_maxdiscountamount numeric,
    p_autoapply boolean,
    p_promocode text,
    p_requirespromocode boolean,
    p_maxtotalredemptions integer,
    p_maxredemptionspercustomer integer,
    p_maxredemptionsperday integer)
RETURNS SETOF "BranchOffers"
LANGUAGE plpgsql AS $$
BEGIN
    INSERT INTO "BranchOffers" (
        "BranchOfferId","TenantId","BranchId","Title","Subtitle","DiscountText","ImageUrl","ImageAltText","DisplayOrder",
        "StartsAtUtc","EndsAtUtc","DiscountTypeCode","DiscountValue","MinimumOrderAmount","MaxDiscountAmount","AutoApply",
        "PromoCode","RequiresPromoCode","MaxTotalRedemptions","MaxRedemptionsPerCustomer","MaxRedemptionsPerDay")
    VALUES (
        p_branchofferid,p_tenantid,p_branchid,p_title,p_subtitle,p_discounttext,p_imageurl,p_imagealttext,p_displayorder,
        p_startsatutc,p_endsatutc,p_discounttypecode,p_discountvalue,p_minimumorderamount,p_maxdiscountamount,p_autoapply,
        NULLIF(upper(btrim(p_promocode)),''),p_requirespromocode,p_maxtotalredemptions,p_maxredemptionspercustomer,p_maxredemptionsperday);

    RETURN QUERY SELECT bo.* FROM "BranchOffers" bo WHERE bo."BranchOfferId"=p_branchofferid;
END;
$$;

