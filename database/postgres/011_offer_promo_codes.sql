        "ImageAltText"=p_imagealttext,
        "DisplayOrder"=p_displayorder,
        "StartsAtUtc"=p_startsatutc,
        "EndsAtUtc"=p_endsatutc,
        "DiscountTypeCode"=p_discounttypecode,
        "DiscountValue"=p_discountvalue,
        "MinimumOrderAmount"=p_minimumorderamount,
        "MaxDiscountAmount"=p_maxdiscountamount,
        "AutoApply"=p_autoapply,
        "IsActive"=p_isactive,
        "PromoCode"=NULLIF(upper(btrim(p_promocode)),''),
        "RequiresPromoCode"=p_requirespromocode,
        "MaxTotalRedemptions"=p_maxtotalredemptions,
        "MaxRedemptionsPerCustomer"=p_maxredemptionspercustomer,
        "MaxRedemptionsPerDay"=p_maxredemptionsperday,
