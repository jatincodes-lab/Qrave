    p_maxdiscountamount numeric,
    p_autoapply boolean,
    p_promocode text,
    p_requirespromocode boolean,
    p_maxtotalredemptions integer,
    p_maxredemptionspercustomer integer,
    p_maxredemptionsperday integer,
    p_isactive boolean)
